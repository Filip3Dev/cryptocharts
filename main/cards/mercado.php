        <p>
          As pessoas dão muita atenção para o que vai a R$ 30,00 ou a R$ 60,00. “O ativo ou  moeda X vai a
R$ 50,00 ou a R$ 10,00? ”. <br>
E concentram nessas questões. É usual em qualquer chat, fórum,
discussão, essa “briga de sardinha”. O sardinha vai junto com a manada, querendo migalha de
alguma coisa, como se lucrasse apenas acertando a tendência do mercado. <br>
Entretanto, a pergunta que deve-se fazer sempre é: se subir, como ganhar dinheiro? Se cair,
como ganhar dinheiro? Porque é certo, independentemente de onde vá, em que ponto vai parar, não
importa, entrar no mercado é para ganhar dinheiro.<br>
 Fora do mercado, é o momento para pensar em
economia, Brasil, empresas, pessoas, ter o comportamento do cidadão comum.
