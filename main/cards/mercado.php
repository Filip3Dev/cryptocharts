      <div class="collapsible-body">
        <p>
          Oportunidade encontra-se todos os dias no gráfico, e todo momento. <br>
          Um belo dia Meu pai falou para mim de uma forma
bem simples: se passar um cavalo arriado ao seu lado, pula em cima dele, porque ele não passa uma
segunda vez.<br> O que ele quis dizer: se você encontrar uma oportunidade na sua vida, aproveite.<br>
No mercado de ações onde passa um “cavalo arriado” a cada cinco minutos do nosso lado.<br>
Às vezes quando perde-se uma oportunidade de trade e é possível que haja sofrimento,
“querer pular em cima desse cavalo”, entrar no meio do movimento do trade, porém, pode
acontecer de machucar-se muito.<br> Então, quando você avistar uma entrada ou quando alguem lhe der uma call, dicas de colegas e etc... analise primeiro se essa entrada se adapta a sua estratégia. <br> Em síntese, não há necessidade de procurar, a
oportunidade quando é realmente boa acaba nos encontrando.
Outro ponto que há resistência em admitir, mas é muito importante ser compreendido:<br> NÃO
é possível acertar mais do que 60% das operações. Então, a cada 10, acerta-se no máximo seis. As
outras 4 darão errado e tomarão stop. Nesse contexto, caso acerte-se apenas essa porcentagem, é
necessário aprender a perder. O que faz vencer no longo prazo é o gerenciamento de risco e de banca.<br> Há pessoas que
acertam 6 de 10 e ganham menos do que quem acerta 4 de 10. Pode até ter os que acertem 9 em 10,
porém, se na operação que errar perder muito mais dinheiro do que ganhou nas outras 9, o resultado
é pouco satisfatório.<br> Assim, preocupe-se com quanto ganha nas operações que darão certo e o
quanto perde-se nas que darão errado.<br> E vai acontecer! Se toda vez que perder houver um prejuízo
de X e ao ganhar, um lucro de 2X ou 3X, no final, a conta fica positiva. Se acontecer de acertar
metade das operações e ganhar pouco e quando errar perder muito a conta nunca vai fechar positiva,
nem se acertar 90% das vezes. É crucial aprender a perder.<br>
