              <p>
                A operação Forex envolve a compra de uma moeda e a simultânea venda de outra, ou seja, as moedas são negociadas em PARES, por exemplo: o Euro e o Dólar (EUR/USD). O investidor não compra dólares ou euros, fisicamente, mas uma relação monetária de troca entre eles.
Assim, quando alguém faz uma operação nesse mercado, não está comprando uma determinada divisa, mas um determinado PAR, uma taxa de câmbio entre as duas moedas. Com a flutuação das taxas e do valor relativo entre as moedas, podem ser estruturadas diferentes estratégias de investimento, que podem resultar em lucros ou em prejuízos.
Normalmente, as cotações das moedas não variam drasticamente em curto espaço de tempo, o que deve gerar dúvidas a respeito da veracidade das promessas de alta rentabilidade que muitas vezes acompanham as ofertas de investimento no FOREX.<br>
Mas então como é possível ter-se lucros elevados neste mercado?<br>
A resposta está na utilização de “margem” para operar, mecanismo que permite negociar um volume maior de dinheiro aplicando apenas uma parte. Como a operação é liquidada apenas pela diferença entre as valorizações de diferentes moedas, não é necessário que o investidor tenha disponível todo o montante de recursos envolvido na operação. O FOREX permite que seja depositada, efectivamente, apenas uma “margem” para cobrir as variações diárias dos pares de moedas.
A margem dá ao investidor maior poder para operar, podendo, assim, realizar operações de grande vulto. Na maioria das Correctoras ou Brokers, a margem é de 100:1,podendo ir até 500:1, permitindo ao investidor / trader fazer uma operação com o valor de referência de 100 mil dólares, para o exemplo de 100:1, depositando apenas 1.000 dólares.
