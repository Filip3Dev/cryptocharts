                O mercado cambial, ou <b>FOREX </b> é o maior mercado mundial em termos de dinheiro movimentado diariamente, com mais de 5 triliões de dólares transacionados diariamente. Para se ter uma ideia da dimensão das transações diárias, podemos comparar com o mercado Bolsista americano que negoceia cerca de 60 Biliões de USD contra os mais de 5 Triliões no FOREX.
É um mercado que trabalha em contínuo, 24 horas por dia, entre as 22h00 GTM de Domingo e as 22 GTM de Sexta. Assim permite um maior volume e mais tempo para realização de operações o que aumenta o volume global e ajuda a que se consiga ter mais operações, e logo mais lucro.
