              <p>
                O mercado não se move de uma forma linear por um período prolongado, na verdade ele é composto de “topos” e “fundos” que, pela sua direção forma uma linha de tendência.<br>
                É justamente a direção destes tops e fundos que vai determinar a tendência, assim como foi dito há mais de 100 atrás na Teoria de Dow.<br>
                O conceito de tendências na análise técnica é essencial, todas as ferramentas utilizadas na análise gráfica como por exemplo (suportes e resistências, indicadores, médias             móveis, etc) têm como único propósito medir as tendências do mercado para então poder tirar proveito delas.<br>
                <h5> Tendência de Alta</h5>
<p>
  Ocorre quando os picos e vales possuem um sentido ascendente, ou seja, a nova formação estará em um nível de preços acima da formação anterior onde o suporte é constituído pela união dos fundos.
</p>
<h5>Tendência de Baixa</h5>
<p>
  Ocorre quando os picos e vales apresentam um sentido descendente, ou seja, a nova formação estará em um nível de preços abaixo da formação anterior onde a resistência é constituída pela união dos topos.
</p>

<h5>Tendência Lateral</h5>
<p>
  Diferente do que muita gente pensa, o mercado não está sempre em alta ou baixa, ele pode estar estável, com uma tendência lateral. Estes momentos ocorrem em períodos de equilíbrio no nível de preços, em que a demanda e a oferta estão em um mesmo patamar. Embora tenhamos definido esta tendência como lateral, é mais comum falar que o mercado está “andando de lado” ou “sem tendência”.
</p>
<h5>Tipos de Tendência</h5>
<p>

Na análise técnica, as tendência são ainda subdivididas em outras três categorias, de acordo com sua duração: tendências primárias, secundárias e Flutuação Diária.<br>
Os critérios que definem cada uma delas variam um pouco, mas em seu fundamento quando criados por Charles Dow, esses critérios eram:<br>
<b>Tendência Principal</b>: é a maré, tendência de mais longo prazo e pode durar até anos, uma vez que a tendência primária se forma, ela permanecerá em vigor até que sinais de reversão sejam confirmados.<br>
<b>Tendência Intermediária</b>: é a onda, tendência mediana formada por movimentos que ocorrem ao longo da tendência principal geralmente contra a mesma.<br>
<b>Flutuações Diárias</b>: é a marola, tendência que dura somente algumas horas ou até dias, caracterizada pelos curtos movimentos que ocorrem em torno de uma tendência secundaria, podem ser perigosas e pouco confiáveis ​​individualmente.<br>
