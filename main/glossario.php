        <div class="hero">

        <p>Nos sites e fóruns sobre bitcoin, aqui e em todo lugar utiliza-se muitos termos técnicos, o que causa confusão.</p>
  <p>Chegou a hora de mudar isso, confira o glossário abaixo com alguns termos que você verá muito por aqui. </p>
        </div>
  <hr>
  <div class="margin">

  <h4>Altcoin</h4>
  <p>Nome dado a moedas alternativas ao bitcoin. Exemplo: Litecoin, Dogecoin, Dash, etc.</p>
  <h4>Bearish</h4>
  <p>
Tendência em um mercado quando, pela predominância da demanda, o preço dos papéis, moedas ou ativos sofre baixa. O termo em inglês vem de "bear" (urso). Um urso ataca com movimentos de cima para baixo, jogando seu rival no chão, daí a origem do termo.
  </p>
  <h4>Bullish</h4>
  <p>
    Tendência em um mercado quando, pela predominância da demanda, o preço dos papéis sofre alta. O termo em inglês vem de "bull" (touro). Um touro ataca com movimentos de baixo para cima, erguendo seu rival no ar, daí a origem do termo.
  </p>
  <h4>Bitcoin</h4>
  <p>Representa o protocolo criado por Satoshi Nakamoto, É a moeda fiat que sera utilizada para fazer trade.</p>
  <h4>Blockchain</h4>
  <p>Cadeia de blocos, aberta onde todos podem ver todas as tranzações que são e ja foram feitas des do primeiro bloco de Bitcoin</p>
  <h4>Blockchain.info</h4>
  <p>Empresa que oferece serviço de carteira e explorador de blocos, erroneamente confundida com a cadeia de blocos do bitcoin.</p>
  <h4>Block Explorer</h4>
  <p>Um block explorer ou blockchain browser, é um site ou programa de computador que permite você visualizar e navegar pelo blockchain do bitcoin e de outras criptomoedas. Os block explorer mostram, de forma amigável e legível ao ser humano, todas as transações, endereços, blocos e outras informações do blockchain.</p>
  <p>Exemplos: <a href="https://www.blockchain.info">Blockchain</a> ou <a href="https://www.blocktrail.com/BTC">Blocktrail</a></p>
  <h4>Bloco Genesis</h4>
  <p>O primeiro bloco do bitcoin, minerado por Satoshi Nakamoto.</p>
  <p>Possui a mensagem “The Times 03/Jan/2009 Chancellor on brink of second bailout for banks”, referente a manchete do jornal The Times, do dia 3 de Janeiro de 2009.</p>
  <h4>BTC</h4>
  <p>Abreviação da unidade monetária do bitcoin.</p>
  <h4>Chart</h4>
  <p>Chart nada mais é do que um pseudônimo, um dos muitos sinônimos de gráfico.</p>
  <h4>Dust transaction</h4>
  <p>Transação com uma pequena quantidade de bitcoins, com baixo valor financeiro, mas que ocupa espaço no blockchain. Na versão 0.8.2 do Bitcoin-Qt, a equipe de core-developers definiu como dust transaction uma transação com output menor que 5.46 uBTC, cerca de R$0,007.</p>
  <h4>Endereço Vanity</h4>
  <p>Um endereço bitcoin personalizado, criado usando o <a href="https://bitcointalk.org/index.php?topic=25804.0">Vanitygen</a>.</p>
  <p>Exemplo: 1FoXBiT54xSBqRQ6Pkh9eiuw9KgG8ixUn2</p>
  <h4>Escrow</h4>
  <p>Nome dado ao ato de manter fundos em posse de terceiros, a fim de proteger durante uma operação.</p>
  <p>Exemplo: João tem bitcoins e Maria tem reais, eles querem negociar, mas não se conhecem muito bem, no entanto, ambos confiam em Carlos. João envia os bitcoins para Carlos, Maria envia os reais para João e, se tudo der certo, Carlos envia os bitcoins para Maria. O Carlos fez o escrow da negociação.</p>
  <h4>Exchange</h4>
  <p>Local utilizado para troca entre moedas e outros ativos. Exchange de bitcoins são utilizadas para trocar bitcoin por moedas FIAT ou outras criptomoedas, e existe é claro exchanges que tem o Bitcoin como a moeda FIAT .</p>
  <h4>Input</h4>
  <p>Endereço origem de uma transação bitcoin. Uma única transação pode ter múltiplos endereços de origem.</p>
  <h4>Liquidez</h4>
  <p>Capacidade de comprar ou vender um ativo facilmente, mesmo em grandes quantidades, volume alto de moeda tanto FIAT quanto a moeda em operação.</p>
  <h4>Long</h4>
  <p>Posição comprada, quando você abre uma posição de compra e lucra com a subida do preço do ativo, moeda, ação e etc...</p>
  <h4>MicroBit – uBTC</h4>
  <p>Milionésima parte de 1 bitcoin ou 0.000001 BTC.</p>
  <h4>miliBit – mBTC</h4>
  <p>Milésima parte de 1 bitcoin ou 0.001 BTC.</p>
  <h4>Minerar</h4>
  <p>Ato de gerar novos bitcoins resolvendo problemas criptográficos com um computador ou hardware específico.</p>
  <h4>Moeda FIAT</h4>
  <p>É a moeda principal, o qual apartir dela você pode comprar outras, moeda que não depende de outras para existir ou para se basear o seu valor.</p>
  <h4>Mixer</h4>
  <p>Serviço utilizado para embaralhar input e output de transações, a fim de manter a privacidade e diminuir o nível de rastreamento.</p>
  <h4>Nó</h4>
  <p>Dispositivo conectado a rede bitcoin que utiliza um programa de computador para retransmitir transações para outros nós, criando uma rede descentralizada.</p>
  <h4></h4>
  <h4>Output</h4>
  <p>Endereço destino de uma transação bitcoin. É possível que uma transação tenha múltiplos outputs.</p>
  <h4>Order Book</h4>
  <p>Lugar onde ficam todas as ordens passivas tanto de compra quanto de venda esperando serem executadas.</p>
  <h4>Pool</h4>
  <p>Coleção de mineradores que se agrupam para minerar coletivamente um bloc, e depois dividir a recompensa entre eles. Pools de mineração são uma ótima maneira para aumentar a probabilidade de êxito conforme a dificuldade aumenta.</p>
  <h4>Pull Back</h4>
  <p>Pull Back tambem chamado de retração é o re-teste do preço a ultima resistência ou suporte.</p>
  <h4>QR code</h4>
  <p>Código de barras bidimensional que pode ser convertido em texto, &nbsp;URL, número de telefone, geolocalização, etc. É muito utilizado para codificar e facilitar a leitura de chaves privadas e endereços bitcoin, por ser facilmente escaneado usando a maioria dos telefones celulares equipados com câmera.</p>
  <h4>Resistência</h4>
  <p>Resistência é um ponto do gráfico idêntificado pela região onde a força vendedora está com força suficiente para não deixar o preço subir <br>
    quando o preço chega proximo dessa região de resistência ele toma força e volta a descer, por isso esse nome.</p>
  <h4>Satoshi</h4>
  <p>Menor divisão de um bitcoin (0.00000001btc).</p>
  <h4>Scamcoin</h4>
  <p>Altcoin criada com objetivos de dar golpe nos usuários e enriquecer os criadores, seja com pump-and-dump ou pré-mineração.</p>
  <h4>Short</h4>
  <p>Posição vendida, quando você abre uma posição de venda e lucra com a queda do preço do ativo, moeda, ação e etc...</p>
  <h4>Suporte</h4>
  <p>Suporte é um ponto do gráfico idêntificado pela região onde a força compradora está com força suficiente para não deixar o preço cair <br>
    quando o preço chega proximo dessa região de suporte ele toma força e volta a subir, por isso esse nome.</p>
  <h4>Stop</h4>
  <p>Ponto de saida de uma operação, é o cinto de segurança de um bom trader onde se o preço for contra a sua posição ele fecha automaticamente a operação.</p>
  <h4>Taxa de Transação, Fee</h4>
  <p>Também conhecida como transaction fee ou mining fee, é uma pequena taxa necessária para uma transação ser processada pelos mineradores.</p>
  <h4>Trade</h4>
  <p>O ato de utilizar estrategias de para lucrar com a compra e a venda de ativos ou moedas, no nosso caso seria de altcoins.</p>
  <h4>Trader</h4>
  <p>Pessoa que faz trade, seja por hobie, seja como meio de trabalho e fonte de renda.</p>
  <h4>Volatilidade</h4>
  <p>Medida dos movimentos de preços ao longo do tempo para um ativo financeiro, comodite e moedas incluindo o Bitcoin.</p>
